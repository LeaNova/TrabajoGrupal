    private void armarCabezera() {
        ArrayList<Object> titulos = new ArrayList<>();
        
        titulos.add("ID");
        titulos.add("Materia");
        titulos.add("Nota");
        
        for (Object titulo: titulos) {
            modelo.addColumn(titulo);
        }
        
        jtabMaterias.setModel(modelo);
    }
    
    private void borrarFila() {
        int filas = modelo.getRowCount() - 1;
        
        for (int cont = filas; cont >= 0; cont--) {
            modelo.removeRow(cont);
        }
    }
    
    private void llenarTabla() {
        Alumno alu = (Alumno)jcAlumno.getSelectedItem();
        int idAlu = alu.getIdAlumno();
        
        List<Inscripcion> inscripcion = id.inscripcionesAlumno(idAlu);
        
        for (Inscripcion insc: inscripcion) {
            modelo.addRow(new Object[]{insc.getMateria().getIdMateria(), insc.getMateria().getNombre(), insc.getNota()});
        }
    }
    
    private void llenarCombo() {
        for (Alumno alu: listaAlumnos) {
            if (alu.isActivo()) {
                jcAlumno.addItem(alu);
            }
        }
    }

