    private void armarCabezera() {
        ArrayList<Object> titulos = new ArrayList<>();
        
        titulos.add("ID");
        titulos.add("Nombre");
        titulos.add("Apellido");
        titulos.add("Activo");
        
        for (Object titulo: titulos) {
            modelo.addColumn(titulo);
        }
        
        jtabAlumnos.setModel(modelo);
    }
    
    private void borrarFila() {
        int filas = modelo.getRowCount() - 1;
        
        for (int cont = filas; cont >= 0; cont--) {
            modelo.removeRow(cont);
        }
    }
    
    private void llenarTabla() {
        Materia mate = (Materia)jcMaterias.getSelectedItem();
        int idMate = mate.getIdMateria();
        
        List<Inscripcion> inscripcion = id.inscripcionesMaterias(idMate);
        
        for (Inscripcion insc: inscripcion) {
            modelo.addRow(new Object[]{insc.getAlumno().getIdAlumno(), insc.getAlumno().getNombre(), insc.getAlumno().getApellido(), insc.getAlumno().isActivo()});
        }
    }
    
    private void llenarCombo() {
        for (Materia mate: listaMaterias) {
            if (mate.isActivo()) {
                jcMaterias.addItem(mate);
            }
        }
    }

