    private void llenarComboAlumnos() {
        for (Alumno alu: listaAlumnos) {
            if (alu.isActivo()) {
                jcAlumno.addItem(alu);
            }
        }
    }
    
    private void llenarComboMaterias() {
        for (Materia mate: listaMaterias) {
            if (mate.isActivo()) {
                jcMaterias.addItem(mate);
            }
        }
    }

